'''
In Python, the append() method is a built-in function that is used to add an element to the end of a list. It modifies the original list by adding the specified element as its last element.
'''

# Here's an example of using the append() method:

# Define an empty list
my_list = []

# Append elements to the list
my_list.append(1)
my_list.append(2)
my_list.append(3)

print(my_list)  # Output: [1, 2, 3]

'''
In this example:

We start with an empty list called my_list.
We use the append() method to add elements 1, 2, and 3 to the end of the list.
After appending the elements, the list becomes [1, 2, 3].
'''

'''The append() method is commonly used when you want to dynamically add elements to a list as you process data or iterate through a loop. It is a convenient way to extend the list by adding elements one at a time.'''