'''

In Python, the replace(), split(), and join() string methods are used for manipulating strings by replacing substrings, splitting strings into substrings, and joining substrings into a single string, respectively.
'''


# replace(): This method replaces all occurrences of a specified substring with another substring within a string.

string = "Hello, World!"
new_string = string.replace("Hello", "Hi")
print(new_string)  # Output: "Hi, World!"
