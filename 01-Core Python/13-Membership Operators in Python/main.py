#In: Evaluates to True if the value or variable is found in the sequence.
my_list = [1, 2, 3, 4, 5]
result = 3 in my_list
print(result)  # result will be True

# Not in: Evaluates to True if the value or variable is not found in the sequence.
my_string = "hello"
result = "a" not in my_string  
print(result)  # result will be True