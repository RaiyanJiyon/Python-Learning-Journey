'''
You can access each character of a string using a loop in Python. There are several loop constructs you can use for this purpose, such as a for loop or a while loop. Here's how you can access each character of a string using a for loop:
'''

my_string = "Hello, World!"

# Using a for loop
for char in my_string:
    print(char)

'''In this code:

We define a string my_string.
We use a for loop to iterate over each character of the string.
Within the loop, the variable char takes on each character of the string one by one, and we print it.
'''