#Bitwise AND (&): Performs a bitwise AND operation between corresponding bits of two operands.
result = 5 & 3  # result will be 1

# Bitwise OR (|): Performs a bitwise OR operation between corresponding bits of two operands.
result = 5 | 3  # result will be 7

# Bitwise XOR (^): Performs a bitwise XOR (exclusive OR) operation between corresponding bits of two operands.
result = 5 ^ 3  # result will be 6

# Bitwise NOT (~): Performs a bitwise inversion, flipping all the bits of the operand.
result = ~5  # result will be -6

# Bitwise Left Shift (<<): Shifts the bits of the first operand to the left by the number of positions specified by the second operand.
result = 5 << 1  # result will be 10

# Bitwise Right Shift (>>): Shifts the bits of the first operand to the right by the number of positions specified by the second operand.
result = 5 >> 1  # result will be 2
